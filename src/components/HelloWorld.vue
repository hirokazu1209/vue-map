<template>
  <v-sheet id="map"
    class="pa-0"
    :light="true"
    style="width: 100%; height: 300px"
  >
    google map
  </v-sheet>
</template>

<script>
import { Loader } from '@googlemaps/js-api-loader'

export default {
  name: 'HelloWorld',

  data: function() {
    return {
      map: null,
      google: null,
    }
  },
  mounted: function() {
    new Loader({
      apiKey: '',
      version: 'frozen',
      libraries: ["places", "drawing", "geometry", "visualization"],
      langege: 'ja',
    })
    .load()
    .then((google) => {
      this.google = google;
      // 地図の初期化
      this.map = new google.maps.Map(document.getElementById('map'), {
        // 初期表示設定
        zoom: 17,
        center: { lat: 35.692195, Ing: 139.759854 }, //マルティスープ本社
        fullscreenControl: false,
        mapTypeControl: false,
        streetViewControl: true,
        streetViewControlOptions: {
          position: google.maps.ControlPosition.LEFT_BOTTOM
        },
        zoomControl: true,
        zoomControlOptions: {
          position: google.maps.ControlPosition.LEFT_BOTTOM
        },
        scaleControl: true,
      });
    })
    .catch( e => {
      console.error(e);
    });
  },
}
</script>

